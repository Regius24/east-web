<template>
  <q-card class="overflow-hidden">
    <!-- TABLE -->
    <q-card-section>
      <div
        class="text-h5 text-weight-light text-center"
        :class="textcolor"
      >{{ title }}</div>
      <div class="text-caption text-weight-light text-center">updated as of: {{ dateCheck }}</div>
      <div
        ref="table"
        class="rounded-borders"
      ></div>
    </q-card-section>

    <!-- FILTERS -->
    <q-card-section class="row justify-center q-col-gutter-sm">
      <div class="col-12 col-md-4">
        <q-select
          dense
          outlined
          v-model="vendor"
          :options="vendors"
          :display-value="`Company Name: ${vendor ? vendor : '*none*'}`"
          :disable="vendorDis"
        />
      </div>

      <div class="col-12 col-md-4">
        <q-select
          dense
          outlined
          v-model="site"
          :options="sites"
          :display-value="`Site: ${site ? site : '*none*'}`"
        />
      </div>

      <div class="col-12 col-md-2">
        <q-btn
          outline
          label="XLSX"
          color="accent"
          class="fit"
          @click="xlsxTable"
        />
      </div>

      <div class="col-12 col-md-2">
        <q-btn
          outline
          label="PDF"
          color="accent"
          class="fit"
          @click="pdfTable"
        />
      </div>
    </q-card-section>

    <!-- <q-card-actions align="right">
      <q-btn
        flat
        label="PDF"
        color="accent"
        @click="pdfTable"
      />
    </q-card-actions> -->
  </q-card>
</template>

<script>
import 'tabulator-tables/dist/css/tabulator.min.css'
import XLSX from 'xlsx/dist/xlsx.full.min.js'
import Tabulator from 'tabulator-tables'
import GetRepo from 'src/repository/get'
import { notify } from 'boot/notifier'
import jsPDF from 'jspdf'
import 'jspdf-autotable'

window.jsPDF = jsPDF
window.XLSX = XLSX

export default {
  props: ['data', 'title', 'date', 'textcolor', 'vendors', 'sites', 'vendorDis'],

  computed: {
    dateCheck () {
      if (this.date == null) {
        return 'N/A'
      } else {
        return this.date.Date
      }
    }
  },

  watch: {
    data (val) {
      this.renderTable()
    },

    vendor (val) { this.$emit('vendorChange', val) },

    site (val) { this.$emit('siteChange', val) }
  },

  data () {
    return {
      tabulator: null,
      vendor: '',
      site: ''
    }
  },

  methods: {
    renderTable () {
      const _this = this
      _this.tabulator = new Tabulator(this.$refs.table, {
        layout: 'fitDataStretch',
        maxHeight: 310,
        index: 'Name',
        data: this.data,
        dataTree: true,
        dataTreeStartExpanded: false,
        groupBy: 'Table',
        groupStartOpen: false,
        groupClosedShowCalcs: true,
        groupHeader: (value, count) => `${value} (${count} items)`,
        groupToggleElement: 'header',
        placeholder: 'No Data Set',
        columns: [
          {
            title: 'LOB',
            field: 'Name',
            sorter: 'string',
            width: '35px'
          },
          {
            title: 'Locked FTE',
            field: 'LockedFte',
            sorter: 'number'
          },
          {
            title: 'Agents',
            field: 'Agents',
            sorter: 'number',
            topCalc: 'sum'
          },
          {
            title: 'Complete',
            field: 'Complete',
            sorter: 'number',
            topCalc: 'sum'
          },
          {
            title: 'Score',
            field: 'Percent',
            sorter: 'number',
            formatter: (cell) => `${cell.getValue().toFixed(2)}%`,
            topCalc: (values, data) => {
              const Agents = data.reduce((x, y) => x + y.Agents, 0)
              const Complete = data.reduce((x, y) => x + y.Complete, 0)
              const Computed = (Complete / Agents) * 100
              return `${Computed.toFixed(2)}%`
            }
          }
        ],
        downloadConfig: {
          rowGroups: true,
          dataTree: false
        }
        // rowClick: (e, row) => {
        //   if (row.getTreeParent()) {
        //     const Vendor = row.getIndex()
        //     const { Name: Lob, Table } = row.getTreeParent().getData()

        //     _this.FetchUamDataFiltered(Lob, Vendor, Table)
        //   } else {
        //     const { Name: Lob, Table } = row.getData()

        //     _this.FetchUamDataFiltered(Lob, '%', Table)
        //   }
        // }
      })

      this.tabulator.setSort([
        { column: 'Table', dir: 'asc' }
      ])
    },

    async FetchUamDataSummary () {
      try {
        const brand = this.title.split(' ')[0]
        const loBrand = brand.toLowerCase()

        const { data } = await GetRepo.UamDataSummary2(loBrand)

        return data
      } catch (err) {
        const statusText = err.response.statusText
        notify('Something went wrong', `Error: ${statusText}`, 'mdi-alert', 'red')
      }
    },

    async FetchUamDataFiltered (lob, vendor, table) {
      const brand = this.title.split(' ')[0].toUpperCase()
      const payload = {
        brand: brand,
        lob: lob,
        vendor: vendor,
        table: table
      }

      this.$q.loadingBar.start()
      notify('Fetching Data', 'Please wait while data loads', 'mdi-timer-sand', 'orange')

      try {
        const { data } = await GetRepo.UamDataAgentsDetailed(payload)

        this.$q.dialog({
          component: () => import('./report-agents-detailed'),
          parent: this,
          data: data,
          title: `${brand} ${lob}`
        })

        this.$q.loadingBar.stop()
      } catch (err) {
        console.log(err)
        this.$q.loadingBar.stop()
      }
    },

    xlsxTable () {
      this.tabulator.download('xlsx', `${this.title}.xlsx`)
    },

    pdfTable () {
      this.tabulator.setGroupStartOpen(true)
      this.tabulator.setGroupBy('Table')

      this.tabulator.download('pdf', `${this.title}.pdf`, {
        orientation: 'landscape',
        title: `${this.title}`,
        autoTable: function (doc) {
          return {
            includeHiddenHtml: true
          }
        }
      })
    },

    htmlTable () {
      this.tabulator.copyToClipboard()
      // console.log(this.tabulator.getHtml())
      // this.tabulator.download('html', 'data.html', { style: true })
    }
  },

  mounted () {
    this.renderTable()
  }
}
</script>
